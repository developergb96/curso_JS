function soma(item1, item2) {
    return item1 + item2;
}
console.log('SOMA: ', soma(5, 6));
console.log('SOMA: ', soma(1000, 16));

function diminuir(item1, item2) {
    return item1 - item2
}
console.log('SUB: ', diminuir(5, 6));
console.log('SUB: ', diminuir(509, 63));

function multi(item1, item2) {
    return item1 * item2
}
console.log('MULTI: ', multi(5, 6));
console.log('MULTI: ', multi(7, 5));